#include "so_long.h"

int	main(int argc, char *argv[])
{
	t_data	data;
	t_game	game;
	t_root	*root;

	if (argc == 1)
	{
		if (read_file(&data, argv[1]))
		{
			if (init_game(&data, &game))
				if (valid_path(&game))
					root = init_root(&game);
		}
	}
	return (0);
}
